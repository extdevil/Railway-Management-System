    //Custom function to load trains from database to UI
    public void LoadTrainsToUI()
    {
		//clearning the JList
        activeTraffic.removeAll();
		
		//Declaring a new List Model to set in JList
        DefaultListModel values = new DefaultListModel();
		
		//Iterating through all elements of the database
        for(int i=0; i<trainsList.size(); i++)
        {
			//Adding the names each Patient name from the currrent database
            values.addElement(trainsList.get(i).Name + "           " +
			trainsList.get(i).StartsFrom + "           " +
			trainsList.get(i).Destination + "           " +
			trainsList.get(i).Timing + "           " +
			trainsList.get(i).TicketPrice + " Rs.");
        }
		
		//Setting our declared model to the JList to show in UI
        activeTraffic.setModel(values);
    }
    
