    //event when find trains button is clicked
    private void findTrainsBtnMouseClicked(java.awt.event.MouseEvent evt) {
		//GEN-FIRST:event_findTrainsBtnMouseClicked
        // TODO add your handling code here:

        //Validating input if blank or not
        if (_liveFrom.getText().isBlank() || _liveTo.getText().isBlank() ||
		_liveDate.getText().isBlank())
        {
            MessageBox("None of the fields can be empty", "Error");
            return;
        }
        
        //validating if terms and conditions are checked or not
        if (!termsConditionsCb.isSelected())
        {
            MessageBox("You must accept our terms and conditions to continue", "Error");
            return;
        }
        
        //getting the train with specified details
        TrainInfo gotTrain = getTrainExist(new TrainInfo("", _liveFrom.getText(),
		_liveTo.getText(), _liveDate.getText(), 0));
        
        //validating if a train is found or not
        if (gotTrain == null)
        {
            MessageBox("No trains found for the selected start and end location !",
			"No trains");
            return;
        }
        
        //Showing trains details after train is found
        MessageBox("Train found for you !\n\nTrain Name : " + gotTrain.Name +
		"\nStarts at : " + gotTrain.StartsFrom + "\nDestination : " +
		gotTrain.Destination + "\nPrice : Rs. " +
		(gotTrain.TicketPrice * (classCb.getSelectedIndex() + 1)),
		"Train found on " + _liveDate.getText());

    }//GEN-LAST:event_findTrainsBtnMouseClicked

    //custom function to get train with specified details
    public TrainInfo getTrainExist(TrainInfo t)
    {
        for (int x=0; x < trainsList.size(); x++)
        {
            if (trainsList.get(x).StartsFrom.toLowerCase().
			startsWith(t.StartsFrom.toLowerCase()) && 
			trainsList.get(x).Destination.toLowerCase().
			startsWith(t.Destination.toLowerCase()) && 
			trainsList.get(x).Timing.toLowerCase().
			startsWith(t.Timing.toLowerCase()))
            {
                return trainsList.get(x);
            }
        }
        
        return null;
    }
    
